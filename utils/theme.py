BORDER_RADIUS_SM = 6
TRANSITION_FAST = "100ms"
TRANSITION_NORMAL = "200ms"
TRANSITION_SLOW = "300ms"


_current_palette = "light"

class Theme:
    @classmethod
    def set_palette(cls, palette_name: str):
        global _current_palette
        _current_palette = palette_name

    @classmethod
    def get_palette(cls) -> str:
        return _current_palette

    @classmethod
    def colors(cls):
        base = DARK if _current_palette == "dark" else LIGHT
        palette = dict(base)
        if _current_palette == "dark":
            palette["DARK_CHOCOLATE"] = "#1B1613"
            palette["ALOEWOOD"] = "#8A6A55"
            palette["MILK_TEA"] = "#D7B89E"
            palette["SAKURA"] = "#E8A5B5"
            palette["MISTY_ROSE"] = "#2F2723"
            palette["WHITE"] = "#F7F3EF"
        else:
            palette["DARK_CHOCOLATE"] = DARK_CHOCOLATE
            palette["ALOEWOOD"] = ALOEWOOD
            palette["MILK_TEA"] = MILK_TEA
            palette["SAKURA"] = SAKURA
            palette["MISTY_ROSE"] = MISTY_ROSE
            palette["WHITE"] = WHITE
        return palette

    @classmethod
    def get(cls, name):
        base = DARK if _current_palette == "dark" else LIGHT
        return base.get(name, "")

    @classmethod
    def glass_card(cls, border_radius: int = None) -> str:
        c = cls.colors()
        br = border_radius or BORDER_RADIUS_LG
        return f"""
            background: {c['GLASS_BG']};
            border: 0.5px solid {c['GLASS_BORDER']};
            border-radius: {br}px;
        """

    @classmethod
    def card_hover_effect(cls) -> str:
        c = cls.colors()
        return f"""
            QFrame#hover_card {{
                {cls.glass_card()}
                transition: all {TRANSITION_NORMAL};
            }}
            QFrame#hover_card:hover {{
                background: {c['CARD_HOVER']};
                border-color: {c['ACCENT']}44;
            }}
        """

    @classmethod
    def stylesheet(cls):
        c = DARK if _current_palette == "dark" else LIGHT
        return f"""
            QToolTip {{
                background: {c['BG']};
                color: {c['TEXT_PRIMARY']};
                border: 1px solid {c['CARD_BORDER']};
                border-radius: {BORDER_RADIUS_SM}px;
                padding: 6px 10px;
                font-family: {FONT_FAMILY};
                font-size: 12px;
            }}
            QScrollBar:vertical {{
                background: {c['SCROLLBAR_BG']};
                width: 6px;
                border: none;
                border-radius: 3px;
            }}
            QScrollBar::handle:vertical {{
                background: {c['SCROLLBAR_HANDLE']};
                border-radius: 3px;
                min-height: 30px;
            }}
            QScrollBar::add-line:vertical, QScrollBar::sub-line:vertical {{
                height: 0;
            }}
            QScrollBar:horizontal {{
                background: {c['SCROLLBAR_BG']};
                height: 6px;
                border: none;
                border-radius: 3px;
            }}
            QScrollBar::handle:horizontal {{
                background: {c['SCROLLBAR_HANDLE']};
                border-radius: 3px;
                min-width: 30px;
            }}
            QScrollBar::add-line:horizontal, QScrollBar::sub-line:horizontal {{
                width: 0;
            }}
            QSplitter::handle {{
                background: {c['DIVIDER']};
                width: 1px;
            }}
        """
